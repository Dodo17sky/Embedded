#define OUTPUT_PINS_NUMBER				2U

static const uint8 outputs[OUTPUT_PINS_NUMBER] =
{
		D7,
		D8
};

/*--------------------------------------------------------------------------------
*                           Functions prototypes declaration
*--------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------
*                           Functions prototypes definition
*--------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------
@name		Controler_InitRunnable
@brief		Initialize different GPIO pins
*--------------------------------------------------------------------------------*/
void Controller_InitRunnable(void)
{
	uint8 idx;
	for(idx=0; idx<OUTPUT_PINS_NUMBER; idx++)
		pinMode(outputs[idx], OUTPUT);
}

/*--------------------------------------------------------------------------------
@name		Controler_MainRunnable
@brief		Main task which controls output pins
*--------------------------------------------------------------------------------*/
void Controller_MainRunnable(void)
{

}

/*--------------------------------------------------------------------------------
@name		Controller_SetOutput
@brief		Set the state of a specific pin
*--------------------------------------------------------------------------------*/
void Controller_SetOutput(uint8 id, uint8 state)
{
	digitalWrite(outputs[id], state);
}
