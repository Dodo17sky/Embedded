#define ZERO_FILL_ARRAY(arr)                                    \
{                                                               \
    int index = 0U;                                             \
    for(index=0; index<(sizeof(arr)/sizeof(arr[0])); index++)   \
    {                                                           \
        arr[index] = 0;                                         \
    }                                                           \
}

#define ARRAY_SIZE(arr)     (sizeof(arr)/sizeof(arr[0]))
    

